class cores():

    def roxo(self,txt):
        return f'\033[4;35;40m{txt}\033[m'
    
    def vermelho(self,txt):
        return f'\033[31m{txt}\033[m'

    def verde(self,txt):
        return f'\033[32m{txt}\033[m'

    def amarelo(self,txt):
        return f'\033[33m{txt}\033[m'

    def azul(self,txt):
        return f'\033[34m{txt}\033[m'